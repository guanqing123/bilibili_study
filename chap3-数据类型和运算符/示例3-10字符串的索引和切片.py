# 字符串又被称为有序的字符序列，对字符串中某个字符的检索称为索引

# 反向递增序号 -N到-1
#   -10      -1
s = 'HELLOWORLD'
#    0        9
# 正向递增序号 0到N-1

print(s[0], s[-10])  # 序号0和序号-10表示的是同一个字符
print('北京欢迎你'[4])  # 获取字符串中索引为4
print('北京欢迎你'[-1])

print('---------------------')
# 对字符串中某个子串或区间的检索称为切片
# 切片的语法结构
# 字符串或字符串变量 [N:M] 从N开始到M结束不包含M

print(s[2:7])  # 从2开始到7结束不包含7 正向递增
print(s[-8:-3])  # 反向递减
print('>>>', s[-3:-8])
print(s[:5])  # 默认N从0开始
print(s[5:])  # M默认是切到字符串的结尾
