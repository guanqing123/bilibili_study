A = {10, 20, 30, 40, 50}
B = {30, 50, 88, 76, 20}
# 交集操作
print('A', A)  # {50, 20, 40, 10, 30}
print('B', B)  # {50, 20, 88, 76, 30}
print('A & B', A & B)  # {50, 20, 30}
# 并集操作
print('A | B', A | B)  # {40, 10, 76, 50, 20, 88, 30}
# 差集操作
print('A - B', A - B)  # {40, 10}
# 补集操作
print(A ^ B)  # {10, 88, 40, 76}
